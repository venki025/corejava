package BasicProgram;

public class SwitchChar {

	public static void main(String[] args) {
		  char ch='z';
		  switch (ch) {
		  case 'a' : System.out.println("Say Hello");
		  break;
		  case 'b' : System.out.println("Say Hiii");
		  break;
		  default:System.out.println("who is this");

	}

	}
}
