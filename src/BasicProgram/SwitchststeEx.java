package BasicProgram;

public class SwitchststeEx {

	public static void main(String[] args) {
		   int a=2;
		   switch(a) {
		   //Case statements
		   case 1:System.out.println("I am @ Home");
		   break;
		   case 2:System.out.println("I am @ PlayGround");
		   break;
		   //Default case  statement
		   default:System.out.println("Not @ Home, or PlayGround");
		   }

	}

}
